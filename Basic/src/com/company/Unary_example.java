package com.company;

public class Unary_example {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		//increment
		System.out.println(x++);
		System.out.println(++y);
		System.out.println(x);
		
        //decrement
		int a = 10;
		int b = 20;
		System.out.println(a--);
		System.out.println(--b);
		System.out.println(a);
		
	}

}
